number = int(input("Введіть 5-значне число: "))

# Розбиваємо число на цифри, використовуючи математичні операції
n1 = number % 10  # Остання цифра
n2 = (number // 10) % 10  # Передостання цифра
n3 = (number // 100) % 10  # Середня цифра
n4 = (number // 1000) % 10  # Друга цифра
n5 = (number // 10000) % 10  # Перша цифра

# Формуємо число у зворотному порядку
reversed_number = n1 * 10000 + n2 * 1000 + n3 * 100 + n4 * 10 + n5

print("Перевернуте число:", reversed_number)