def move_last_to_first(lst):
    if len(lst) > 1:  # Перевірка, чи є більше одного елемента в списку
        return [lst[-1]] + lst[:-1]  # Додаємо останній елемент на початок і залишаємо решту
    return lst  # Якщо список порожній або складається з одного елемента, повертаємо його без змін

# Приклади використання
lists = [
    [12, 3, 4, 10],          # Тест 1
    [1],                     # Тест 2
    [],                      # Тест 3
    [12, 3, 4, 10, 8]        # Тест 4
]

# Обробка кожного списку
for lst in lists:
    result = move_last_to_first(lst)
    print(f"Вхідний список: {lst} => Результат: {result}")

